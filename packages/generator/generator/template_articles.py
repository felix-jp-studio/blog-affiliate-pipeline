def slugify(keyword: str) -> str:
    mapping = {
        "格安SIM 20GB おすすめ": "sim-20gb-osusume",
        "楽天モバイル 乗り換え 手順": "rakuten-mobile-switch",
        "MNP 予約番号 取得方法": "mnp-reservation-number",
        "格安SIM 速度 遅い 対処": "sim-speed-slow-fix",
        "LINEMO 評判 デメリット": "linemo-hyoban-demerit",
    }
    if keyword in mapping:
        return mapping[keyword]
    base = re.sub(r"[^a-zA-Z0-9]+", "-", unicodedata.normalize("NFKC", keyword)).strip("-")
    return base.lower()[:60] or "article"


def build_outline(item: dict) -> dict:
    keyword = item["keyword"]
    article_type = item["articleType"]
    category = item["category"]

    titles = {
        "格安SIM 20GB おすすめ": "格安SIM 20GBの選び方と比較ポイント【2026年7月版】",
        "楽天モバイル 乗り換え 手順": "楽天モバイルへの乗り換え手順をわかりやすく解説",
        "MNP 予約番号 取得方法": "MNP予約番号の取得方法と注意点をまとめて解説",
        "格安SIM 速度 遅い 対処": "格安SIMの速度が遅いときの対処法と確認手順",
        "LINEMO 評判 デメリット": "LINEMOの評判とデメリットを中立に整理",
    }

    return {
        "title": titles.get(keyword, f"{keyword}の完全ガイド"),
        "slug": slugify(keyword),
        "metaDescription": f"{keyword}について、公式情報を参照しながら中立に解説します。料金・条件は各公式サイトでご確認ください。",
        "category": category,
        "articleType": article_type,
        "keyword": keyword,
        "ctaCarriers": ["rakuten-mobile", "linemo", "ahamo", "povo", "uq-mobile"],
    }


def build_body(outline: dict) -> str:
    builders = {
        "comparison": _comparison_body,
        "howto": _howto_body,
        "troubleshoot": _troubleshoot_body,
    }
    fn = builders[outline["articleType"]]
    body = fn(outline)
    return _ensure_min_length(body, outline["keyword"], min_chars=3600)


